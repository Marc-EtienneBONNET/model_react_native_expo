const TEXT_VARIANT_WEIGHTS = [
	"Semibold",
	"Normal",
	"Medium",
	"Light",
	"Bold",
] as const;

type TypeTextVariantWeight = (typeof TEXT_VARIANT_WEIGHTS)[number];

type TypeTextVariantWeightKey = Lowercase<TypeTextVariantWeight>;

type TypeExtractSize<K extends string> =
	K extends `${infer S}${TypeTextVariantWeight}` ? S : never;

type TypeTextVariantSize = TypeExtractSize<
	Exclude<keyof typeof textVariant, "defaults">
>;

type TypeEnumTextVariant = {
	[S in TypeTextVariantSize]: {
		[W in TypeTextVariantWeightKey]: `${S}${Capitalize<W>}`;
	};
};

function buildEnumTextVariant(): TypeEnumTextVariant {
	const result: Record<string, Record<string, string>> = {};
	for (const key of Object.keys(textVariant)) {
		if (key === "defaults") continue;
		for (const weight of TEXT_VARIANT_WEIGHTS) {
			if (key.endsWith(weight)) {
				const size = key.slice(0, -weight.length);
				if (!result[size]) result[size] = {};
				result[size][weight.toLowerCase()] = key;
				break;
			}
		}
	}
	return result as TypeEnumTextVariant;
}

export const EnumTextVariant = buildEnumTextVariant();
